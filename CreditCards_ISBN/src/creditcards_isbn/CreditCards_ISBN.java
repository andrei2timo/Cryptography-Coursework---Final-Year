    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // VERIFICATION button for ISBN
        
        int[]digit = new int[10];   // An array of the digits(chars) of the input string
        
        String input;
        input = jTextField2.getText();   // Get input from user
        System.out.println(input);
    
        int i = input.length();
        if(i == 10){      // Check if the input is in the correct format (10 digits)
            
            for(int n=0; n < 9; n++){
                digit[n] = Integer.parseInt(String.valueOf(input.charAt(n)));   // Place input in array as chars (separate digits)
            }    // The loop is only for the first 9 digits, as the last may need special treatment
            
            String test = String.valueOf(input.charAt(9));  // If the last char is (X) -> substitution for 10
            if("X".equals(test)){
                digit[9] = 10;        // X = 10, therefore the last digit is 10
            }
            else{ 
                digit[9] = Integer.parseInt(String.valueOf(input.charAt(9)));   // If the last char IS int, add it to the array
            }
            
            int sum = digit[0] + 2*digit[1] + 3*digit[2] + 4*digit[3] + 5*digit[4] + 6*digit[5] + 7*digit[6]
                + 8*digit[7] + 9*digit[8];  // Sum up all the digits
            
            int mod = sum%11;
            if(mod == digit[9]){   // if the sum of digits mod 11 = last digit, success
                String result = "Verification successful!";
                jTextArea1.setText(result);
            }
            else{
                String result = "Verification failed !";
                jTextArea1.setText(result);
            }
            jTextField2.setText("");   // Clear the field and label when VERIFY button pressed
            jLabel1.setText("");
        }
        else{
            jLabel1.setText("Invalid input. Please enter 10 symbols!");   // IF input is in invalid format, print error message
        }  
    }//GEN-LAST:event_jButton4ActionPerformed

