    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // VERIFICATION button for CREDIT CARD
       
        int[]digit = new int[16];    // An array of the digits(chars) of the input string
        
        String input;
        input = jTextField1.getText();  // Get input from user
        System.out.println(input);
        
        try{                    // The program WILL NOT break if a letter/symbol is entered
            int len = input.length(); 
            if(len == 16){        // Check if the input is in the correct format (16 digits)
            
                for(int n=0; n<16; n++){
                    digit[n] = Integer.parseInt(String.valueOf(input.charAt(n)));  // Place input into digits array (char by char)
                }
            
                int sum = digit[1] + digit[3] + digit[5] + digit[7] + digit[9]
                    + digit[11] + digit[13] + digit[15];  // Sum up all numbers at an even position (2nd, 4th, 6th..), as they don't have to be multiplied
                
                int res;
                for(int num = 0; num < 16; num += 2){  // For the digits at odd positions
                    if(digit[num] >= 5){               // If the number equals or is greater than 5
                        res = 2*digit[num] - 9;         // 9 is substracted from the number's multiplication by 2 and the result is summed with the rest
                        sum += res;
                    }
                    else{         // If the number is smaller than 5
                        res = 2*digit[num];   // It is multiplied by 2 and the result is summed with the rest
                        sum += res;
                    }
                }
                //System.out.println(sum);
                int mod = sum%10;
                if(mod == 0){         // When there is no reminder, validation is successful
                    String result = "Verification successful!";
                    jTextArea1.setText(result);
                }
                else{                        // If there is a reminder, verification has failed
                    String result = "Verification failed !";
                    jTextArea1.setText(result);
                }
                jTextField1.setText("");   // When button is pressed, field and label are cleared
                jLabel1.setText("");
            }
            else{
                jLabel1.setText("Invalid input. Please enter 16 symbols!");  // If input format is INVALID
            }
        }
        catch(NumberFormatException e){          // IF the input contains a symbol different than INTEGER
            jLabel1.setText("Invalid input. Please enter digits only.");
        }
        
            
    }//GEN-LAST:event_jButton1MouseClicked

